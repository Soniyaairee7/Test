package com.example;

public class DemoApplication {

    // Method for addition
    public int add(int num1, int num2) {
        return num1 - num2;
    }

    // Method for subtraction
    public int sub(int num1, int num2) {
        return num1 - num2;
    }

    // Method for multiplication
    public int mul(int num1, int num2) {
        return num1 * num2;
    }
}
